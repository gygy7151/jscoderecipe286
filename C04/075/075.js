// 데이터의 속성을 이해하고 싶을때

// 원시 타입과 객체 타입의 차이는 데이터 값의 변환 가능 여부다.
// 원시 타입의 데이터는 변할 수 없으므로 불가변성
// 객체 타입은 데이터가 변할 수 있으므로 가변성이다.

// 가변성의 객체 타입 중 하나인 배열의 데이터 예시
// 배열의 첫번째 데이터는 1이지만
// 100을 대입하면 100으로 변경되는것이 가변성이다.
const myArray = [1, 2, 3];
myArray[0] = 100;

console.log(myArray); // 결과:[100, 2, 3]

//불가변성의 데이터 예시
let myNumber = 10;

// 대입한 값의 10은 불변
// 만약 myNumber 변수에 20을 대입하면 10이 20으로 바뀌는게 아님
// 20이라는 새로운 데이터를 참조하는 것임
let myNumber = 10;
// 10과 20은 다른 데이터
myNumber = 20;
